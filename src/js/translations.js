// Idioma padrão
let currentLanguage = 'pt';

// Função para aplicar traduções
function applyTranslations(lang) {
    currentLanguage = lang;
    const t = translations[lang];

    // Navigation
    document.querySelectorAll('[data-i18n="navMenu"]').forEach(el => el.innerHTML = t.navMenu);
    document.querySelectorAll('[data-i18n="navAbout"]').forEach(el => el.innerHTML = t.navAbout);
    document.querySelectorAll('[data-i18n="navTechs"]').forEach(el => el.innerHTML = t.navTechs);
    document.querySelectorAll('[data-i18n="navHobby"]').forEach(el => el.innerHTML = t.navHobby);
    document.querySelectorAll('[data-i18n="navPortfolio"]').forEach(el => el.innerHTML = t.navPortfolio);
    document.querySelectorAll('[data-i18n="navToggle"]').forEach(el => el.innerHTML = t.navToggle);

    // Header
    const nameEl = document.querySelector('[data-i18n="name"]');
    if (nameEl) nameEl.innerHTML = t.name;

    // Tecnologias do header (já existe a função renderTechnologies)
    const techContainer = document.querySelector('.masthead-subheading');
    if (techContainer) {
        techContainer.innerHTML = t.technologies.join('&nbsp;&nbsp;&nbsp;&nbsp;|&nbsp;&nbsp;&nbsp;&nbsp;');
    }

    // About Section
    const aboutTitle = document.querySelector('[data-i18n="aboutTitle"]');
    if (aboutTitle) aboutTitle.innerHTML = t.aboutTitle;

    const aboutText1 = document.querySelector('[data-i18n="aboutText1"]');
    if (aboutText1) aboutText1.innerHTML = t.aboutText1;

    const aboutText2 = document.querySelector('[data-i18n="aboutText2"]');
    if (aboutText2) aboutText2.innerHTML = t.aboutText2;

    // Technologies Section
    const techsTitle = document.querySelector('[data-i18n="techsTitle"]');
    if (techsTitle) techsTitle.innerHTML = t.techsTitle;

    const techsText1 = document.querySelector('[data-i18n="techsText1"]');
    if (techsText1) techsText1.innerHTML = t.techsText1;

    const techsText2 = document.querySelector('[data-i18n="techsText2"]');
    if (techsText2) techsText2.innerHTML = t.techsText2;

    const techsText3 = document.querySelector('[data-i18n="techsText3"]');
    if (techsText3) techsText3.innerHTML = t.techsText3;

    // Hobbies Section
    const hobbyTitle = document.querySelector('[data-i18n="hobbyTitle"]');
    if (hobbyTitle) hobbyTitle.innerHTML = t.hobbyTitle;

    const hobbyText = document.querySelector('[data-i18n="hobbyText"]');
    if (hobbyText) hobbyText.innerHTML = t.hobbyText;

    // Portfolio Section
    const portfolioTitle = document.querySelector('[data-i18n="portfolioTitle"]');
    if (portfolioTitle) portfolioTitle.innerHTML = t.portfolioTitle;

    const stockflowDesc = document.querySelector('[data-i18n="stockflowDesc"]');
    if (stockflowDesc) stockflowDesc.innerHTML = t.stockflowDesc;

    // Footer
    const footerSocial = document.querySelector('[data-i18n="footerSocial"]');
    if (footerSocial) footerSocial.innerHTML = t.footerSocial;

    const footerResumeBtn = document.querySelector('[data-i18n="footerResumeBtn"]');
    if (footerResumeBtn) footerResumeBtn.innerHTML = t.footerResumeBtn;

    const footerRights = document.querySelector('[data-i18n="footerRights"]');
    if (footerRights) footerRights.innerHTML = t.footerRights;

    // Atualizar o link do currículo
    const resumeLink = document.querySelector('[data-i18n="resumeLink"]');
    if (resumeLink && t.resumeFile) {
        resumeLink.href = t.resumeFile;
    }

    // Atualizar o select de idioma
    const languageSelect = document.querySelector('.language-selection');
    if (languageSelect) {
        languageSelect.value = lang;
    }

    // Salvar preferência no localStorage
    localStorage.setItem('preferredLanguage', lang);
}

// Função para trocar idioma
function changeLanguage(lang) {
    if (translations[lang]) {
        applyTranslations(lang);
    }
}

// Inicializar com idioma salvo ou padrão
function initializeLanguage() {
    const savedLang = localStorage.getItem('preferredLanguage') || 'pt';
    applyTranslations(savedLang);
}

// Exportar funções para uso global
if (typeof window !== 'undefined') {
    window.changeLanguage = changeLanguage;
    window.applyTranslations = applyTranslations;
    window.translations = translations;
}
